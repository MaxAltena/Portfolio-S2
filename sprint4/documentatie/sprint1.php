        <h1>Concept</h1>
        <p>Mijn website zal mijn leerportfolio voor het tweede semester van ICT &amp; Media Design weergeven. Het belangrijkste van de website is om op een dynamische wijze categorieën, gemaakte werken en opdrachten in te laden en weer te geven. Bij categorieën is het mogelijk om een rubrix toe te voegen en ook om een stukje tekst over de categorie. Bij opdrachten en gemaakte werken kunnen er foto's en documenten worden toegevoegd als referentie materiaal.</p>
        <p>Verschillende categorieën komen in het menu te staan. Hiernaast worden alle categorieën weergegeven onder op de homepagina. Op de categorie pagina worden alle opdrachten en werken geladen die de bepaalde categorie hebben. Hiernaast kan er worden gefilterd op sprints. Bij een categorie is er een mogelijkheid om een rubrix toe te voegen.</p>
        <p>Al de data, categorieën, opdrachten, rubrix' en gebruikers, wordt in een MySQL database opgeslagen. Data kan worden verwijderd, toegevoegd of gewijzigd wanneer je bent ingelogd in het CMS systeem. </p>
        <p>Met de gemaakte portfoliowebsite wil ik laten zien dat ik goed ben in programmeren, creatief ben en oog heb voor de gebruiker. Onder het kopje prototype staat een prototype van de eerste versie van de website.</p>
